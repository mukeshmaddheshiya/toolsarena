export function toNepaliNum(n: number): string {
  return String(n).split('').map(d => NP_DIGITS[parseInt(d)] || d).join('');
}

// Reference: BS 2000/1/1 = AD 1943/4/14
const BS_REF = { year: 2000, month: 0, day: 1 }; // month 0-indexed
const AD_REF = new Date(1943, 3, 14); // April 14, 1943

function totalDaysInBSYear(year: number): number {
  const months = BS_CALENDAR[year];
  if (!months) return 0;
  return months.reduce((a, b) => a + b, 0);
}

/** Convert BS date to AD date */
export function bsToAd(bsYear: number, bsMonth: number, bsDay: number): Date | null {
  if (!BS_CALENDAR[bsYear]) return null;
  const months = BS_CALENDAR[bsYear];
  if (bsMonth < 1 || bsMonth > 12) return null;
  if (bsDay < 1 || bsDay > months[bsMonth - 1]) return null;

  // Count total days from BS reference to given date
  let totalDays = 0;
  for (let y = BS_REF.year; y < bsYear; y++) {
    totalDays += totalDaysInBSYear(y);
  }
  for (let m = 0; m < bsMonth - 1; m++) {
    totalDays += months[m];
  }
  totalDays += bsDay - 1;

  const result = new Date(AD_REF);
  result.setDate(result.getDate() + totalDays);
  return result;
}

/** Convert AD date to BS date */
export function adToBs(adDate: Date): { year: number; month: number; day: number } | null {
  const diffTime = adDate.getTime() - AD_REF.getTime();
  if (diffTime < 0) return null;
  let totalDays = Math.round(diffTime / (1000 * 60 * 60 * 24));

  let bsYear = BS_REF.year;
  let bsMonth = 1;
  let bsDay = 1;

  // Find year
  while (bsYear <= 2099) {
    const daysInYear = totalDaysInBSYear(bsYear);
    if (daysInYear === 0) return null;
    if (totalDays < daysInYear) break;
    totalDays -= daysInYear;
    bsYear++;
  }
  if (bsYear > 2099) return null;

  // Find month
  const months = BS_CALENDAR[bsYear];
  if (!months) return null;
  let monthIdx = 0;
  while (monthIdx < 12 && totalDays >= months[monthIdx]) {
    totalDays -= months[monthIdx];
    monthIdx++;
  }
  if (monthIdx >= 12) return null;

  bsMonth = monthIdx + 1;
  bsDay = totalDays + 1;

  return { year: bsYear, month: bsMonth, day: bsDay };
}
